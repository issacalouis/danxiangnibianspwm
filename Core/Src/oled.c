/* ============================================================
 *  OLED_Clear / OLED_Refresh
 * ============================================================ */
void OLED_Clear(void)
{
    memset(oled_buf, 0, sizeof(oled_buf));
}

void OLED_Refresh(void)
{
    for (uint8_t page = 0; page < OLED_PAGES; page++) {
        OLED_WriteCmd(0xB0 + page);
        OLED_WriteCmd(0x00);
        OLED_WriteCmd(0x10);
        OLED_DC_Set();
        OLED_CS_Clr();
        HAL_SPI_Transmit(&hspi3, oled_buf[page], OLED_WIDTH, 1000);
        OLED_CS_Set();
    }
}

/* ============================================================
 *  OLED_DrawPoint
 * ============================================================ */
void OLED_DrawPoint(uint8_t x, uint8_t y, uint8_t color)
{
    if (x >= OLED_WIDTH || y >= OLED_HEIGHT) return;
    if (color)
        oled_buf[y / 8][x] |=  (1 << (y % 8));
    else
        oled_buf[y / 8][x] &= ~(1 << (y % 8));
}

/* ============================================================
 *  OLED_ShowChar  (8?16)
 *  size ???????????????8?16??
 * ============================================================ */
void OLED_ShowChar(uint8_t x, uint8_t y, char ch, uint8_t size)
{
    (void)size;
    if (ch < ' ' || ch > 'Z') ch = ' ';
    uint8_t idx = ch - ' ';

    for (uint8_t i = 0; i < 8; i++) {
        uint8_t col_top = Font8x16[idx][i];
        uint8_t col_bot = Font8x16[idx][i + 8];
        for (uint8_t bit = 0; bit < 8; bit++) {
            OLED_DrawPoint(x + i, y + bit,     (col_top >> bit) & 0x01);
            OLED_DrawPoint(x + i, y + bit + 8, (col_bot >> bit) & 0x01);
        }
    }
}

void OLED_ShowString(uint8_t x, uint8_t y, const char *str, uint8_t size)
{
    while (*str) {
        OLED_ShowChar(x, y, *str++, size);
        x += 8;
        if (x + 8 > OLED_WIDTH) { x = 0; y += 16; }
    }
}

/* ============================================================
 *  OLED_ShowFloat
 * ============================================================ */
void OLED_ShowFloat(uint8_t x, uint8_t y, float val, uint8_t decimals, uint8_t size)
{
    char buf[16];
    if (decimals == 1)      snprintf(buf, sizeof(buf), "%.1f", (double)val);
    else if (decimals == 2) snprintf(buf, sizeof(buf), "%.2f", (double)val);
    else if (decimals == 4) snprintf(buf, sizeof(buf), "%.4f", (double)val);
    else                    snprintf(buf, sizeof(buf), "%.0f", (double)val);
    OLED_ShowString(x, y, buf, size);
}

void OLED_ShowInt(uint8_t x, uint8_t y, int32_t val, uint8_t size)
{
    char buf[12];
    snprintf(buf, sizeof(buf), "%ld", (long)val);
    OLED_ShowString(x, y, buf, size);
}

/* ============================================================
 *  OLED_DrawLine  (Bresenham)
 * ============================================================ */
void OLED_DrawLine(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1)
{
    int16_t dx = (int16_t)x1 - x0, dy = (int16_t)y1 - y0;
    int16_t sx = dx > 0 ? 1 : -1, sy = dy > 0 ? 1 : -1;
    if (dx < 0) dx = -dx;
    if (dy < 0) dy = -dy;
    int16_t err = dx - dy;
    int16_t cx = x0, cy = y0;
    while (1) {
        OLED_DrawPoint((uint8_t)cx, (uint8_t)cy, 1);
        if (cx == x1 && cy == y1) break;
        int16_t e2 = 2 * err;
        if (e2 > -dy) { err -= dy; cx += sx; }
        if (e2 <  dx) { err += dx; cy += sy; }
    }
}

/* ============================================================
 *  OLED_DrawRect
 * ============================================================ */
void OLED_DrawRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, uint8_t fill)
{
    if (fill) {
        for (uint8_t j = y; j < y + h && j < OLED_HEIGHT; j++)
            for (uint8_t i = x; i < x + w && i < OLED_WIDTH; i++)
                OLED_DrawPoint(i, j, 1);
    } else {
        OLED_DrawLine(x,       y,       x+w-1, y      );
        OLED_DrawLine(x,       y+h-1,   x+w-1, y+h-1  );
        OLED_DrawLine(x,       y,       x,     y+h-1  );
        OLED_DrawLine(x+w-1,   y,       x+w-1, y+h-1  );
    }
}

/* ============================================================
 *  OLED_BoostUI  -  Inverter control interface
 * ============================================================ */
void OLED_BoostUI(float v_target, float i_limit, float output_freq_hz, float v_out, float i_out, float i_in, float modulation, uint8_t edit_mode, uint8_t run_state)
{
    (void)i_in;
    OLED_Clear();

    if (run_state == 3U) {
        OLED_ShowString(0, 0, "OC", 16);
    } else if (run_state == 2U) {
        OLED_ShowString(0, 0, "RUN", 16);
    } else if (run_state == 1U) {
        OLED_ShowString(0, 0, "ARM", 16);
    } else {
        OLED_ShowString(0, 0, "STOP", 16);
    }

    if (edit_mode == 0U) {
        OLED_ShowString(72, 0, "SET V", 16);
    } else if (edit_mode == 2U) {
        OLED_ShowString(72, 0, "SET F", 16);
    } else {
        OLED_ShowString(72, 0, "SET I", 16);
    }

    OLED_ShowString(0, 16, "SV:", 16);
    OLED_ShowFloat(24, 16, v_target, 1, 16);
    OLED_ShowString(72, 16, "F:", 16);
    OLED_ShowFloat(96, 16, output_freq_hz, 0, 16);

    OLED_ShowString(0, 32, "Vr:", 16);
    OLED_ShowFloat(24, 32, v_out, 1, 16);
    OLED_ShowString(64, 32, "Ir:", 16);
    OLED_ShowFloat(88, 32, i_out, 1, 16);

    OLED_ShowString(0, 48, "LI:", 16);
    OLED_ShowFloat(24, 48, i_limit, 1, 16);
    OLED_ShowString(72, 48, "M:", 16);
    OLED_ShowFloat(88, 48, modulation, 2, 16);

    OLED_Refresh();
}

void OLED_BoostCalUI(uint8_t cal_mode, float bias, float value)
{
    OLED_Clear();

    uint8_t gain_mode = (cal_mode >= 4U && cal_mode <= 6U) ? 1U : 0U;

    if (cal_mode == 1U) {
        OLED_ShowString(0, 0, "CAL V", 16);
    } else if (cal_mode == 2U) {
        OLED_ShowString(0, 0, "CAL IO", 16);
    } else if (cal_mode == 3U) {
        OLED_ShowString(0, 0, "CAL II", 16);
    } else if (cal_mode == 4U) {
        OLED_ShowString(0, 0, "CAL VG", 16);
    } else if (cal_mode == 5U) {
        OLED_ShowString(0, 0, "CAL IOG", 16);
    } else if (cal_mode == 6U) {
        OLED_ShowString(0, 0, "CAL IIG", 16);
    } else {
        OLED_ShowString(0, 0, "CAL", 16);
    }

    OLED_ShowString(0, 16, gain_mode ? "GAIN:" : "BIAS:", 16);
    OLED_ShowFloat(48, 16, bias, gain_mode ? 1 : 4, 16);

    OLED_ShowString(0, 32, "VAL:", 16);
    OLED_ShowFloat(40, 32, value, 1, 16);

    OLED_ShowString(0, 48, gain_mode ? "1/2 GAIN" : "1/2 BIAS", 16);
    OLED_Refresh();
}
